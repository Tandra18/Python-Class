# A simple tuple
my_tuple = (1, 2, 3, 3)
print(my_tuple)
print(type(my_tuple))

# A tuple with mixed data types
info = ("Alice", 25, True)
print(info)
print(len(info))

# A nested tuple
nested = (1, (2, 3), 4)
print(nested)
print(len(nested))

# Single element tuple (note the comma)
one_item = (5,)
print(one_item)
print(len(one_item))

basket  = tuple(["apple","banana","orange"])
print(basket)
